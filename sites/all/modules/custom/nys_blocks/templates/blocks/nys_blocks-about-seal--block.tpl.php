	<img class="c-seal--img" src="<?php print base_path() . drupal_get_path('theme', 'nysenate'); ?>/images/fpo-about-seal.jpg" />
	<p>In 1885, the 1850's version of the New York State Coat of Arms was selected by the Legislature as the "permanent design and legal form." The seal symbolizes the following:</p>
	<ul>
		<li>In the center, a shield reveals the sun rising behind Mount Beacon over the Hudson River. “The shield symbolizes in the full sun the name and idea of Old York and the old world; the mountains, river and meadow, with the ships, convey the name and idea of New York in the new world.”</li>
		<li>To the right, Justice is ready to fight tyranny with her sword held high.</li>
		<li>Liberty on the left, holds her foot on the overthrown English Crown. “This New York is supported by Justice and Liberty, and discards monarchy.”</li>
		<li>The world globe is displayed above the shield. “By exhibiting the eastern and western continents on the globe, the old and new are brought together;”</li>
		<li>Above the world globe soars the eagle. “while the eagle on the crest proclaims,” Westward the course of empire takes its way.”</li>
		<li>The bottom ribbon exclaims “Excelsior,” which means “still higher” or “ever upward.”</li>
	</ul>
